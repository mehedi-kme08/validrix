def test_web_test_generator_additional_branches(monkeypatch: pytest.MonkeyPatch) -> None:
    anthropic_calls: list[dict[str, object]] = []
    openai_calls: list[dict[str, object]] = []

    class FakeAnthropic:
        def __init__(self, api_key: str, timeout: int) -> None:
            self.messages = SimpleNamespace(create=self.create)

        def create(self, **kwargs: object) -> object:
            anthropic_calls.append(kwargs)
            return SimpleNamespace(content=[SimpleNamespace(text="anthropic code")])

    class FakeOpenAI:
        def __init__(self, api_key: str, timeout: int) -> None:
            self.chat = SimpleNamespace(completions=SimpleNamespace(create=self.create))

        def create(self, **kwargs: object) -> object:
            openai_calls.append(kwargs)
            return SimpleNamespace(choices=[SimpleNamespace(message=SimpleNamespace(content="openai code"))])

    monkeypatch.setattr(generator_module.anthropic, "Anthropic", FakeAnthropic)
    monkeypatch.setattr(generator_module.openai, "OpenAI", FakeOpenAI)
    assert _call_anthropic("sys", "user", AIConfig(anthropic_api_key="k")) == "anthropic code"
    assert _call_openai("sys", "user", AIConfig(provider="openai", openai_api_key="k")) == "openai code"
    assert anthropic_calls and openai_calls

    summary = _build_crawl_summary(CrawlResult(url="https://example.com"))
    assert "TITLE: (no title)" in summary
    assert "META DESCRIPTION: (none)" in summary
    external_only_summary = _build_crawl_summary(
        CrawlResult(
            url="https://example.com",
            links=[LinkElement(text="External", href="https://other.com", is_external=True)],
        )
    )
    assert "INTERNAL LINKS" not in external_only_summary

    parsed = _parse_tests_from_code("def helper() -> None:\n    pass\n")
    assert parsed[0].name == "test_generated_suite"

    monkeypatch.setattr(generator_module.ConfigManager, "load", lambda: FrameworkConfig())
    anthropic_generator = WebTestGenerator(ai_config=AIConfig(anthropic_api_key="x", provider="anthropic"))
    monkeypatch.setattr(
        generator_module,
        "_call_anthropic",
        lambda system, user, config: "anthropic",
    )
    assert anthropic_generator._call_ai("prompt") == "anthropic"

    generator = WebTestGenerator(ai_config=AIConfig())
    monkeypatch.setattr(
        generator,
        "_call_ai",
        lambda prompt: "```python\ndef test_ok(page: Page) -> None:\n    page.goto(BASE_URL)\n```",
    )
    suite = generator.generate(CrawlResult(url="https://example.com"), prompt="goal")
    assert suite.succeeded is True
